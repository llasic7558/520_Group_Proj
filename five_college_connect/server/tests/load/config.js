export function pickRandom(items) {
  return items[Math.floor(Math.random() * items.length)];
}

export function matchesExpectedUser(payload, account) {
  if (!account.expectedUserId) {
    return Boolean(payload?.user?.id);
  }

  return payload?.user?.id === account.expectedUserId;
}

