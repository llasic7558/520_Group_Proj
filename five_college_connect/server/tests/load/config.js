export function buildThresholds(p95Ms = 800, failedRate = 0.02) {
  return {
    http_req_failed: [`rate<${failedRate}`],
    http_req_duration: [`p(95)<${p95Ms}`, `p(99)<${Math.max(p95Ms * 2, p95Ms + 500)}`],
    checks: ["rate>0.99"],
    fcc_api_checks: ["rate>0.99"]
  };
}

export function jsonParams(token) {
  const headers = {
    "Content-Type": "application/json"
  };

  if (token) {
    headers.Authorization = `Bearer ${token}`;
  }

  return { headers };
}

export function think(minSeconds = defaultThinkTimeMin, maxSeconds = defaultThinkTimeMax) {
  const max = Math.max(maxSeconds, minSeconds);
  const delay = minSeconds + Math.random() * (max - minSeconds);
  sleep(delay);
}

export function parseJson(response) {
  try {
    return response.json();
  } catch {
    return null;
  }
}

export function recordTrend(trend, response) {
  trend.add(response.timings.duration);
}

export function requestJson(name, method, path, body = null, params = undefined) {
  const response = http.request(
    method,
    `${baseUrl}${path}`,
    body ? JSON.stringify(body) : null,
    params
  );
  const ok = check(response, {
    [`${name} returned JSON`]: (res) => {
      const contentType = String(res.headers["Content-Type"] || "");
      return contentType.includes("application/json");
    }
  });

  apiChecks.add(ok);
  return response;
}

export function signInAccount(account) {
  const response = requestJson("signin", "POST", "/api/auth/signin", {
    email: account.email,
    password: defaultPassword
  }, jsonParams());

  recordTrend(authLatency, response);

  const payload = parseJson(response);
  const ok = check(response, {
    "signin status is 200": (res) => res.status === 200,
    "signin returns auth token": () => Boolean(payload?.authToken),
    "signin returns expected user": () => matchesExpectedUser(payload, account)
  });

  apiChecks.add(ok);

  if (!ok) {
    fail(`Unable to sign in seeded account ${account.email}`);
  }

  return {
    ...account,
    token: payload.authToken,
    userId: payload.user.id
  };
}

